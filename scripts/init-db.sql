-- 3.1 Forecasts — clip on GeneratedAt (the production-time of the row).
IF OBJECT_ID(N'dbo.fv_forecasts_at_cursor', N'IF') IS NOT NULL
    DROP FUNCTION dbo.fv_forecasts_at_cursor;
GO
CREATE FUNCTION dbo.fv_forecasts_at_cursor (@asOf DATETIME2(3))
RETURNS TABLE
AS
RETURN
(
    SELECT  ForecastId,
            GeneratedAt,
            TargetTime,
            PredictedTemperature,
            PredictedHumidity,
            ConfidenceLowerTemp,
            ConfidenceUpperTemp,
            ModelVersion
    FROM    dbo.Forecasts
    WHERE   GeneratedAt <= @asOf
);
GO

-- 3.2 Anomalies — clip on DetectedAt (when the detector saw it). Reading-time
--     remains the event timestamp; cursor governs visibility of the detection.
IF OBJECT_ID(N'dbo.fv_anomalies_at_cursor', N'IF') IS NOT NULL
    DROP FUNCTION dbo.fv_anomalies_at_cursor;
GO
CREATE FUNCTION dbo.fv_anomalies_at_cursor (@asOf DATETIME2(3))
RETURNS TABLE
