        CONSTRAINT UQ_ComfortScores_Bucket UNIQUE (BucketTime),
        CONSTRAINT CK_ComfortScores_Rating CHECK
            (Rating IN (N'excellent', N'acceptable', N'marginal', N'uncomfortable')),
        CONSTRAINT CK_ComfortScores_Season CHECK (Season IN (N'summer', N'winter')),
        CONSTRAINT CK_ComfortScores_Score CHECK (Score BETWEEN 0 AND 100)
    );
END;
GO

-- 2.6 AlertRules — human-defined threshold rules. Seeded with three defaults.
IF OBJECT_ID(N'dbo.AlertRules', N'U') IS NULL
BEGIN
    CREATE TABLE dbo.AlertRules
    (
        RuleId          INT IDENTITY(1,1) NOT NULL,
        Name            NVARCHAR(128) NOT NULL,
        Metric          VARCHAR(16) NOT NULL,
        Operator        VARCHAR(2) NOT NULL,
        Threshold       DECIMAL(7, 3) NOT NULL,
        DurationMinutes INT NOT NULL,
        Enabled         BIT NOT NULL CONSTRAINT DF_AlertRules_Enabled DEFAULT (1),
        CreatedAt       DATETIME2(3) NOT NULL CONSTRAINT DF_AlertRules_CreatedAt DEFAULT (SYSUTCDATETIME()),
        CONSTRAINT PK_AlertRules PRIMARY KEY CLUSTERED (RuleId),
        CONSTRAINT UQ_AlertRules_Name UNIQUE (Name),
        CONSTRAINT CK_AlertRules_Metric CHECK (Metric IN (N'T', N'RH')),
        CONSTRAINT CK_AlertRules_Operator CHECK (Operator IN (N'>', N'<')),
        CONSTRAINT CK_AlertRules_Duration CHECK (DurationMinutes > 0)
    );
END;
GO

-- 2.7 Alerts — closed breach intervals; append-only.
IF OBJECT_ID(N'dbo.Alerts', N'U') IS NULL
BEGIN
    CREATE TABLE dbo.Alerts
    (
        AlertId           BIGINT IDENTITY(1,1) NOT NULL,
        RuleId            INT NOT NULL,
        BreachStart       DATETIME2(3) NOT NULL,
        BreachEnd         DATETIME2(3) NOT NULL,
        PeakValue         DECIMAL(7, 3) NOT NULL,
        ReplayClockAtFire DATETIME2(3) NOT NULL,
        CONSTRAINT PK_Alerts PRIMARY KEY CLUSTERED (AlertId),
        CONSTRAINT UQ_Alerts_RuleStart UNIQUE (RuleId, BreachStart),
        CONSTRAINT FK_Alerts_AlertRules FOREIGN KEY (RuleId)
            REFERENCES dbo.AlertRules (RuleId)
    );

    CREATE NONCLUSTERED INDEX IX_Alerts_ReplayClockAtFire
        ON dbo.Alerts (ReplayClockAtFire);
END;
GO

-- 2.8 Leaderboard — heterogeneous: notebook rows + one live row.
--
--     `Provenance` distinguishes the two origins (CONTEXT.md → "Provenance").
IF OBJECT_ID(N'dbo.Leaderboard', N'U') IS NULL
BEGIN
    CREATE TABLE dbo.Leaderboard
    (
        LeaderboardId BIGINT IDENTITY(1,1) NOT NULL,
        ModelName     VARCHAR(64) NOT NULL,
        Mae           DECIMAL(8, 4) NOT NULL,
        Rmse          DECIMAL(8, 4) NOT NULL,
        Mape          DECIMAL(8, 4) NULL,
        Smape         DECIMAL(8, 4) NULL,
