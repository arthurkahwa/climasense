-- =====================================================================
-- ClimaSense — canonical schema (Slice 1: Foundation).
--
-- This script is the SQL-first schema authority. It is:
--
--   * Idempotent. Re-running on an existing database is a no-op for every
--     object; CREATE...IF NOT EXISTS is emulated via metadata guards
--     because SQL Server lacks a true `CREATE TABLE IF NOT EXISTS`.
--
--   * Empty-by-design at slice 1. No data is inserted here other than
--     three seed alert rules that live alongside their schema. CSV
--     ingestion lands in slice 3 (#5) via `bcp`.
--
--   * The single source of truth. EF Core scaffolds POCOs from this DB
--     (Database-First); SQLAlchemy reflects via `autoload_with=engine`.
--     There is NO migration tool.
--
-- Provenance:
--   Numeric constants in the `Pattern` CASE expression and threshold
--   alert seeds carry inline comments citing the notebook source. When
--   a specific cell could not be identified the citation reads
--   "from notebook EDA section §X" instead — see the slice-1 PR's
--   "Judgment calls" for unresolved citations.
-- =====================================================================

SET NOCOUNT ON;
SET ANSI_NULLS ON;
SET QUOTED_IDENTIFIER ON;

-- ---------------------------------------------------------------------
-- 1. Database
-- ---------------------------------------------------------------------
IF DB_ID(N'ClimaSense') IS NULL
BEGIN
    CREATE DATABASE [ClimaSense];
END;
GO

USE [ClimaSense];
GO

-- ---------------------------------------------------------------------
-- 2. Tables
-- ---------------------------------------------------------------------

-- 2.1 SensorReadings — the raw mirrored stream.
--
--     PK is non-clustered on `Id` so the table can be physically
--     ordered by `ReadingTime` for sequential time-series scans.
IF OBJECT_ID(N'dbo.SensorReadings', N'U') IS NULL
BEGIN
    CREATE TABLE dbo.SensorReadings
    (
        Id          BIGINT IDENTITY(1,1) NOT NULL,
        ReadingTime DATETIME2(3) NOT NULL,
        Temperature DECIMAL(6, 3) NOT NULL,
        Humidity    DECIMAL(6, 3) NOT NULL,
        CONSTRAINT PK_SensorReadings PRIMARY KEY NONCLUSTERED (Id)
    );

    CREATE CLUSTERED INDEX IX_SensorReadings_ReadingTime
        ON dbo.SensorReadings (ReadingTime);

    CREATE NONCLUSTERED INDEX IX_SensorReadings_Temperature
        ON dbo.SensorReadings (Temperature);

    CREATE NONCLUSTERED INDEX IX_SensorReadings_Humidity
        ON dbo.SensorReadings (Humidity);
END;
GO

-- 2.2 Anomalies — typed events from the three-detector pipeline.
--
--     UNIQUE(AnomalyType, ReadingTime) makes per-type idempotency a
--     schema property, not caller discipline.
IF OBJECT_ID(N'dbo.Anomalies', N'U') IS NULL
BEGIN
    CREATE TABLE dbo.Anomalies
    (
        AnomalyId   BIGINT IDENTITY(1,1) NOT NULL,
        ReadingTime DATETIME2(3) NOT NULL,
        AnomalyType VARCHAR(32) NOT NULL,
        Severity    DECIMAL(8, 4) NOT NULL,
        Score       DECIMAL(8, 4) NULL,
        Description NVARCHAR(512) NULL,
        DetectedAt  DATETIME2(3) NOT NULL CONSTRAINT DF_Anomalies_DetectedAt DEFAULT (SYSUTCDATETIME()),
        CONSTRAINT PK_Anomalies PRIMARY KEY CLUSTERED (AnomalyId),
        CONSTRAINT UQ_Anomalies_TypeTime UNIQUE (AnomalyType, ReadingTime),
        CONSTRAINT CK_Anomalies_Type CHECK
            (AnomalyType IN (N'sensor_failure', N'regime_shift', N'residual_outlier'))
    );

    CREATE NONCLUSTERED INDEX IX_Anomalies_ReadingTime ON dbo.Anomalies (ReadingTime);
    CREATE NONCLUSTERED INDEX IX_Anomalies_DetectedAt  ON dbo.Anomalies (DetectedAt);
END;
GO

-- 2.3 Forecasts — generated by `LagLinearForecaster` (and possibly future
--     siblings). `ModelVersion` records the producing implementation.
--
--     Read queries clip with `WHERE GeneratedAt <= @asOf` so forecasts
--     produced at any cursor position remain correct historical artefacts.
IF OBJECT_ID(N'dbo.Forecasts', N'U') IS NULL
BEGIN
    CREATE TABLE dbo.Forecasts
    (
        ForecastId           BIGINT IDENTITY(1,1) NOT NULL,
        GeneratedAt          DATETIME2(3) NOT NULL,
        TargetTime           DATETIME2(3) NOT NULL,
        PredictedTemperature DECIMAL(7, 3) NOT NULL,
        PredictedHumidity    DECIMAL(7, 3) NOT NULL,
        ConfidenceLowerTemp  DECIMAL(7, 3) NULL,
        ConfidenceUpperTemp  DECIMAL(7, 3) NULL,
        ModelVersion         VARCHAR(64) NOT NULL,
        CONSTRAINT PK_Forecasts PRIMARY KEY CLUSTERED (ForecastId)
    );

    CREATE NONCLUSTERED INDEX IX_Forecasts_GeneratedAt
        ON dbo.Forecasts (GeneratedAt) INCLUDE (TargetTime);

    CREATE NONCLUSTERED INDEX IX_Forecasts_TargetTime
        ON dbo.Forecasts (TargetTime);
END;
GO

-- 2.4 DayProfiles — calendar-conditioned z-scores; one row per (Date).
IF OBJECT_ID(N'dbo.DayProfiles', N'U') IS NULL
BEGIN
    CREATE TABLE dbo.DayProfiles
    (
        DayProfileId   BIGINT IDENTITY(1,1) NOT NULL,
        [Date]         DATE NOT NULL,
        DayOfWeek      TINYINT NOT NULL,
        MeanResidual   DECIMAL(8, 4) NOT NULL,
        MaxAbsZscore   DECIMAL(8, 4) NOT NULL,
        Pattern        VARCHAR(16) NOT NULL,
        ComputedAt     DATETIME2(3) NOT NULL CONSTRAINT DF_DayProfiles_ComputedAt DEFAULT (SYSUTCDATETIME()),
        CONSTRAINT PK_DayProfiles PRIMARY KEY CLUSTERED (DayProfileId),
        CONSTRAINT UQ_DayProfiles_Date UNIQUE ([Date]),
        CONSTRAINT CK_DayProfiles_Pattern CHECK
            (Pattern IN (N'quiet', N'warm', N'cool', N'volatile')),
        CONSTRAINT CK_DayProfiles_DayOfWeek CHECK (DayOfWeek BETWEEN 0 AND 6)
    );
END;
GO

-- 2.5 ComfortScores — ASHRAE 55 graphical comfort zone evaluation per bucket.
IF OBJECT_ID(N'dbo.ComfortScores', N'U') IS NULL
